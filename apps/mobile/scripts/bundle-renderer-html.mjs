#!/usr/bin/env node
import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const appRoot = join(__dirname, '..');
const distDir = join(appRoot, 'dist');
const srcDir = join(appRoot, 'src');
const htmlPath = join(distDir, 'index.html');

let html = readFileSync(htmlPath, 'utf8');

function readDistAsset(relativePath) {
  return readFileSync(join(distDir, relativePath.replace(/^\.\//, '')), 'utf8');
}

html = html.replace(/<script\b[^>]*\bsrc="\.\/([^"]+)"[^>]*><\/script>/g, (match, assetPath) => {
  if (!/\btype="module"/.test(match)) return match;
  const js = readDistAsset(assetPath);
  return `<script type="module">\n${js}\n</script>`;
});
html = html.replace(/<link\b[^>]*\bhref="\.\/([^"]+)"[^>]*>/g, (match, assetPath) => {
  if (!/\brel="stylesheet"/.test(match)) return match;
  const css = readDistAsset(assetPath);
  return `<style>\n${css}\n</style>`;
});

// Inline common image/font assets referenced by relative URLs in the generated CSS/HTML.
for (const dir of ['assets']) {
  const full = join(distDir, dir);
  try {
    for (const file of readdirSync(full)) {
      const rel = `${dir}/${file}`;
      if (!html.includes(rel) && !html.includes(`./${rel}`)) continue;
      const data = readFileSync(join(full, file));
      const ext = file.split('.').pop()?.toLowerCase();
      const mime =
        ext === 'png' ? 'image/png' :
        ext === 'jpg' || ext === 'jpeg' ? 'image/jpeg' :
        ext === 'webp' ? 'image/webp' :
        ext === 'svg' ? 'image/svg+xml' :
        ext === 'woff2' ? 'font/woff2' :
        ext === 'woff' ? 'font/woff' :
        'application/octet-stream';
      const uri = `data:${mime};base64,${data.toString('base64')}`;
      html = html.split(`./${rel}`).join(uri).split(rel).join(uri);
    }
  } catch {
    // no assets dir
  }
}

const outPath = join(srcDir, 'bundled-renderer-html.ts');
writeFileSync(
  outPath,
  `// Generated by scripts/bundle-renderer-html.mjs. Do not edit by hand.\n` +
    `export const bundledRendererHtml = ${JSON.stringify(html)};\n`
);
console.log(`Wrote ${outPath} (${Buffer.byteLength(html, 'utf8')} bytes of HTML)`);
